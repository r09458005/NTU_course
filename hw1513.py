#他在匆忙之中列下了每道料理不足的食材和數量。
#由於每道料理用的的材料有可能重複，列下的清單中可能有重複的食材名稱。
#請幫他把相同食材的購買數量加總，並找出需求量最大的食材。

a=int(input())
d={};
for i in range(a):
    a=input().split()
    if a[0] in d:
        d[a[0]]+=int(a[1])
    else:
        d[a[0]]=int(a[1])

a=max(d, key=d.get)
print(a,d[a])
